-- Creating a Function to extract the actual tenant_id from JWT token payload 
-- Supabase JWT injects app_metadata, assuming RED COMERCIAL python backend puts 'tenant_id' in there
CREATE OR REPLACE FUNCTION public.get_tenant_id() RETURNS uuid
    LANGUAGE sql STABLE
    AS $$
  select nullif(current_setting('request.jwt.claim.app_metadata', true)::json->>'tenant_id', '')::uuid;
$$;


-- Applying tenant isolation policies to everything except superadmin and public tables
-- (Assuming operations done by backend Service Role bypass these policies automatically)
CREATE POLICY "Tenants can only read own data" ON public.clients FOR ALL
  USING (tenant_id = public.get_tenant_id());
  
CREATE POLICY "Tenants can only read own products" ON public.products FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenants can only read own sales" ON public.vendas FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenants can only read own transactions" ON public.transactions FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenant isolation for user details" ON public.tenant_users FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenant isolation for generic sales" ON public.sales FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenant isolation for orders" ON public.orders FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenant isolation for restaurant tables" ON public.tables FOR ALL
  USING (tenant_id = public.get_tenant_id());

CREATE POLICY "Tenant permissions on themselves" ON public.tenants FOR SELECT
  USING (id = public.get_tenant_id());

