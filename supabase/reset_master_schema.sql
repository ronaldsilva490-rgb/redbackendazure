-- Alias product -> produtos (Used in sales_v2.py)
CREATE VIEW public.produtos AS 
  SELECT * FROM public.products;

-- View for low stock items (produtos_estoque_baixo)
CREATE VIEW public.produtos_estoque_baixo AS 
  SELECT * FROM public.products
  WHERE estoque_atual <= estoque_minimo;

-- View for defaulting clients (clientes_inadimplentes) 
-- Joining clients with overdue transactions
CREATE VIEW public.clientes_inadimplentes AS
  SELECT DISTINCT c.* 
  FROM public.clients c
  INNER JOIN public.transactions t ON t.beneficiario = c.nome
  WHERE t.pago = false AND t.data_vencimento < CURRENT_DATE AND t.tipo = 'receita';


